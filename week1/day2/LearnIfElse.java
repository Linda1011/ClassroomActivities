package week1.day2;

public class LearnIfElse {

	public static void main(String[] args) {
		
		int num = -12;
		if(num>0) {
			System.out.println(num+" is a positive number");
	    }
		else if (num==0){
			System.out.println(num+" is neutral");
		}
		else {
			System.out.println(num+" is a negative num");
		}
		
	}

}
